def compute_tau_contribution_by_bin(
    result: dict[str, Any],
    refractive_index_file: str | Path,
    wavelength_um: np.ndarray | None = None,
    n_wavelength: int | None = None,
    wavelength_range_um: tuple[float, float] | None = None,
    optics_mode: str = "compact_mie",
    refrind_mapping: str = "auto",
    refrind_wave_unit: str = "auto",
    refrind_extrapolate: str = "clamp",
    verbose: bool = True,
) -> dict[str, Any]:
    """
    Compute the vertical optical-depth contribution from each particle bin.

    This diagnostic recomputes the direct-Mie opacity on the Caligo grid, but
    keeps the contribution from each particle-size bin separate. It is meant
    to answer questions like:

        Which bins dominate the haze optical depth?
        Are very large porous aggregate radii controlling the spectrum?
        Does compact_mie or aggregate_effective_mie shift opacity into
        different particle sizes?

    Returns
    -------
    dict
        Contains wavelength_um, vertical_tau, tau_by_bin, bin-level fractions,
        number and mass columns, and a representative optical radius for each
        bin.
    """
    if "ctx" not in result:
        raise KeyError("result must contain result['ctx'].")

    if "n" not in result:
        raise KeyError("result must contain result['n'].")

    ctx = result["ctx"]
    n_state = np.asarray(result["n"], dtype=float)

    Pbar = np.asarray(ctx["Pbar"], dtype=float)
    dz = np.asarray(ctx["dz"], dtype=float)
    n_bin = int(ctx["n_bin"])
    nz = int(ctx["nz"])

    if n_state.shape != (nz, n_bin):
        raise ValueError(
            "result['n'] has the wrong shape. "
            f"Got {n_state.shape}, expected {(nz, n_bin)}."
        )

    if dz.shape != (nz,):
        raise ValueError("ctx['dz'] must have shape (nz,).")

    refrind = read_refractive_index_file(
        refractive_index_file,
        mapping=refrind_mapping,
        wave_unit=refrind_wave_unit,
        verbose=verbose,
    )

    if wavelength_um is None:
        if wavelength_range_um is not None:
            lo, hi = wavelength_range_um
            if lo <= 0 or hi <= lo:
                raise ValueError("wavelength_range_um must be (positive_min, larger_max).")

            nwave = int(n_wavelength or 180)
            wavelength_um = np.logspace(np.log10(lo), np.log10(hi), nwave)

        elif n_wavelength is not None:
            wavelength_um = np.logspace(
                np.log10(float(refrind.wavelength_um.min())),
                np.log10(float(refrind.wavelength_um.max())),
                int(n_wavelength),
            )

        else:
            wavelength_um = refrind.wavelength_um.copy()

    else:
        wavelength_um = _as_array_1d(wavelength_um, "wavelength_um")

    wavelength_um = _as_array_1d(wavelength_um, "wavelength_um")

    if not np.all(wavelength_um > 0):
        raise ValueError("wavelength_um must be positive.")

    order_wave = np.argsort(wavelength_um)
    wavelength_um = wavelength_um[order_wave]

    m_solid_wave, refrind_diag = interpolate_refractive_index(
        refrind,
        wavelength_um,
        extrapolate=refrind_extrapolate,
    )

    radius_matrix_cm = _select_radius_matrix_cm(ctx, optics_mode)

    if radius_matrix_cm.shape != (nz, n_bin):
        raise ValueError("Internal radius matrix has the wrong shape.")

    rho_haze = float(ctx.get("rho_haze", 1.0))

    if optics_mode == "aggregate_effective_mie":
        rho_eff_layer = np.asarray(ctx["rho_eff_layer"], dtype=float)

        if rho_eff_layer.shape != (nz, n_bin):
            raise ValueError("ctx['rho_eff_layer'] must have shape (nz, n_bin).")

        filling_matrix = np.clip(rho_eff_layer / max(rho_haze, 1e-300), 0.0, 1.0)

        can_be_aggregate = np.asarray(
            ctx.get("can_be_aggregate", np.zeros(n_bin, dtype=bool)),
            dtype=bool,
        )

        w_agg_layer = np.asarray(ctx.get("w_agg_layer", np.zeros(nz)), dtype=float)
        w_agg_matrix = np.clip(w_agg_layer[:, None], 0.0, 1.0) * can_be_aggregate[None, :]

    else:
        filling_matrix = np.ones((nz, n_bin), dtype=float)
        w_agg_matrix = np.zeros((nz, n_bin), dtype=float)

    nwave = wavelength_um.size

    tau_by_bin = np.zeros((n_bin, nwave), dtype=float)
    tau_by_layer_bin = np.zeros((nz, n_bin, nwave), dtype=float)

    number_column_by_layer_bin = n_state * dz[:, None]
    number_column_by_bin = np.sum(number_column_by_layer_bin, axis=0)

    m_bin = np.asarray(ctx["m_bin"], dtype=float)
    mass_column_by_bin = np.sum(n_state * m_bin[None, :] * dz[:, None], axis=0)

    # Representative radius per bin, weighted by particle number column.
    radius_nm_matrix = radius_matrix_cm * 1e7
    radius_nm_by_bin = np.zeros(n_bin, dtype=float)

    for i in range(n_bin):
        weights = number_column_by_layer_bin[:, i]

        if np.sum(weights) > 0:
            radius_nm_by_bin[i] = float(
                np.sum(radius_nm_matrix[:, i] * weights)
                / np.maximum(np.sum(weights), 1e-300)
            )
        else:
            radius_nm_by_bin[i] = float(np.nanmedian(radius_nm_matrix[:, i]))

    compact_radius_nm = np.asarray(ctx["r_compact_nm"], dtype=float)

    if verbose:
        print("Bin-level direct-Mie optical-depth diagnostic")
        print("  optics_mode:", optics_mode)
        print("  layers:", nz)
        print("  bins:", n_bin)
        print("  wavelength points:", nwave)
        print("  wavelength range [um]:", float(wavelength_um.min()), "to", float(wavelength_um.max()))
        print("  representative optical radius range [nm]:",
              float(np.nanmin(radius_nm_by_bin)), "to", float(np.nanmax(radius_nm_by_bin)))

    for k in range(nz):
        for i in range(n_bin):
            Ncol = float(number_column_by_layer_bin[k, i])

            if Ncol <= 0 or not np.isfinite(Ncol):
                continue

            radius_cm = float(radius_matrix_cm[k, i])

            if radius_cm <= 0 or not np.isfinite(radius_cm):
                continue

            radius_um = radius_cm * 1e4
            area_cm2 = np.pi * radius_cm**2

            if optics_mode == "aggregate_effective_mie":
                f_fill = float(filling_matrix[k, i])
                m_wave = _maxwell_garnett_effective_index(m_solid_wave, f_fill)
            else:
                m_wave = m_solid_wave

            for j, wave_um in enumerate(wavelength_um):
                qext, _qsca, _g = _mie_efficiencies_one(m_wave[j], radius_um, float(wave_um))

                if qext <= 0 or not np.isfinite(qext):
                    continue

                tau = Ncol * area_cm2 * qext
                tau_by_layer_bin[k, i, j] = tau
                tau_by_bin[i, j] += tau

    vertical_tau = np.sum(tau_by_bin, axis=0)

    if np.any(vertical_tau > 0):
        peak_index = int(np.nanargmax(vertical_tau))
    else:
        peak_index = 0

    tau_by_bin_at_peak = tau_by_bin[:, peak_index]
    total_peak_tau = float(np.sum(tau_by_bin_at_peak))

    tau_fraction_by_bin_at_peak = (
        tau_by_bin_at_peak
        / np.maximum(total_peak_tau, 1e-300)
    )

    # Integrate over log wavelength so the diagnostic is less sensitive to
    # uneven wavelength spacing.
    log_wave = np.log(np.maximum(wavelength_um, 1e-300))

    if nwave > 1:
        tau_by_bin_integrated = np.trapz(tau_by_bin, x=log_wave, axis=1)
    else:
        tau_by_bin_integrated = tau_by_bin[:, 0].copy()

    tau_by_bin_integrated = np.maximum(tau_by_bin_integrated, 0.0)
    total_integrated_tau = float(np.sum(tau_by_bin_integrated))

    tau_fraction_by_bin_integrated = (
        tau_by_bin_integrated
        / np.maximum(total_integrated_tau, 1e-300)
    )

    giant_radius_mask = radius_nm_by_bin > 1.0e4

    diag = {
        "optics_mode": optics_mode,
        "n_layer_micro": int(nz),
        "n_bin": int(n_bin),
        "n_wavelength": int(nwave),
        "wavelength_min_um": float(wavelength_um.min()),
        "wavelength_max_um": float(wavelength_um.max()),
        "peak_wavelength_um": float(wavelength_um[peak_index]),
        "max_vertical_tau": float(np.nanmax(vertical_tau)),
        "total_peak_tau": float(total_peak_tau),
        "total_integrated_tau_logwave": float(total_integrated_tau),
        "representative_radius_min_nm": float(np.nanmin(radius_nm_by_bin)),
        "representative_radius_max_nm": float(np.nanmax(radius_nm_by_bin)),
        "fraction_peak_tau_radius_gt_10000_nm": float(
            np.sum(tau_fraction_by_bin_at_peak[giant_radius_mask])
        ),
        "fraction_integrated_tau_radius_gt_10000_nm": float(
            np.sum(tau_fraction_by_bin_integrated[giant_radius_mask])
        ),
        "total_number_column_cm2": float(np.sum(number_column_by_bin)),
        "total_mass_column_g_cm2": float(np.sum(mass_column_by_bin)),
    }

    diag.update(refrind_diag)

    if optics_mode == "aggregate_effective_mie":
        diag.update(
            {
                "filling_factor_min": float(np.nanmin(filling_matrix)),
                "filling_factor_max": float(np.nanmax(filling_matrix)),
                "aggregate_weight_min": float(np.nanmin(w_agg_matrix)),
                "aggregate_weight_max": float(np.nanmax(w_agg_matrix)),
                "aggregate_effective_mie_note": (
                    "Porous-sphere Maxwell-Garnett approximation. "
                    "Not a full fractal-aggregate scattering calculation."
                ),
            }
        )

    if verbose:
        print("  max vertical tau:", diag["max_vertical_tau"])
        print("  peak wavelength [um]:", diag["peak_wavelength_um"])
        print(
            "  fraction of peak tau from representative radius > 10,000 nm:",
            diag["fraction_peak_tau_radius_gt_10000_nm"],
        )
        print(
            "  fraction of integrated tau from representative radius > 10,000 nm:",
            diag["fraction_integrated_tau_radius_gt_10000_nm"],
        )

    return {
        "ctx": ctx,
        "Pbar_micro": Pbar,
        "dz_micro": dz,
        "n_state": n_state,
        "wavelength_um": wavelength_um,
        "wavenumber": 1e4 / wavelength_um,
        "tau_by_bin": tau_by_bin,
        "tau_by_layer_bin": tau_by_layer_bin,
        "vertical_tau": vertical_tau,
        "peak_index": peak_index,
        "peak_wavelength_um": float(wavelength_um[peak_index]),
        "tau_by_bin_at_peak": tau_by_bin_at_peak,
        "tau_fraction_by_bin_at_peak": tau_fraction_by_bin_at_peak,
        "tau_by_bin_integrated": tau_by_bin_integrated,
        "tau_fraction_by_bin_integrated": tau_fraction_by_bin_integrated,
        "compact_radius_nm": compact_radius_nm,
        "radius_nm_by_bin": radius_nm_by_bin,
        "number_column_by_bin": number_column_by_bin,
        "mass_column_by_bin": mass_column_by_bin,
        "radius_matrix_cm": radius_matrix_cm,
        "diag": diag,
    }


def compare_compact_and_aggregate_optics(
    result: dict[str, Any],
    refractive_index_file: str | Path,
    wavelength_um: np.ndarray | None = None,
    n_wavelength: int | None = None,
    wavelength_range_um: tuple[float, float] | None = None,
    refrind_mapping: str = "auto",
    refrind_wave_unit: str = "auto",
    refrind_extrapolate: str = "clamp",
    verbose: bool = True,
) -> dict[str, Any]:
    """
    Compare compact_mie and aggregate_effective_mie on the same Caligo result.

    This uses the same wavelength grid for both calculations and returns the
    vertical optical depths and aggregate/compact ratio.
    """
    compact = compute_layer_optical_properties(
        result=result,
        refractive_index_file=refractive_index_file,
        wavelength_um=wavelength_um,
        n_wavelength=n_wavelength,
        wavelength_range_um=wavelength_range_um,
        optics_mode="compact_mie",
        refrind_mapping=refrind_mapping,
        refrind_wave_unit=refrind_wave_unit,
        refrind_extrapolate=refrind_extrapolate,
        verbose=verbose,
    )

    aggregate = compute_layer_optical_properties(
        result=result,
        refractive_index_file=refractive_index_file,
        wavelength_um=compact["wavelength_um"],
        optics_mode="aggregate_effective_mie",
        refrind_mapping=refrind_mapping,
        refrind_wave_unit=refrind_wave_unit,
        refrind_extrapolate=refrind_extrapolate,
        verbose=verbose,
    )

    wave = compact["wavelength_um"]
    tau_compact = compact["vertical_tau"]
    tau_aggregate = aggregate["vertical_tau"]

    ratio = tau_aggregate / np.maximum(tau_compact, 1e-300)

    if wave.size > 1:
        int_compact = float(np.trapz(tau_compact, x=np.log(wave)))
        int_aggregate = float(np.trapz(tau_aggregate, x=np.log(wave)))
    else:
        int_compact = float(tau_compact[0])
        int_aggregate = float(tau_aggregate[0])

    peak_compact_idx = int(np.nanargmax(tau_compact)) if np.any(tau_compact > 0) else 0
    peak_aggregate_idx = int(np.nanargmax(tau_aggregate)) if np.any(tau_aggregate > 0) else 0

    diag = {
        "max_tau_compact": float(np.nanmax(tau_compact)),
        "max_tau_aggregate_effective": float(np.nanmax(tau_aggregate)),
        "peak_wavelength_compact_um": float(wave[peak_compact_idx]),
        "peak_wavelength_aggregate_effective_um": float(wave[peak_aggregate_idx]),
        "integrated_tau_compact_logwave": float(max(int_compact, 0.0)),
        "integrated_tau_aggregate_effective_logwave": float(max(int_aggregate, 0.0)),
        "integrated_tau_ratio_aggregate_over_compact": float(
            max(int_aggregate, 0.0) / max(max(int_compact, 0.0), 1e-300)
        ),
        "max_ratio_aggregate_over_compact": float(np.nanmax(ratio)),
        "min_ratio_aggregate_over_compact": float(np.nanmin(ratio)),
        "median_ratio_aggregate_over_compact": float(np.nanmedian(ratio)),
    }

    if verbose:
        print("Compact vs aggregate-effective optics comparison")
        for key, value in diag.items():
            print(f"  {key}: {value}")

    return {
        "compact": compact,
        "aggregate_effective": aggregate,
        "wavelength_um": wave,
        "tau_compact": tau_compact,
        "tau_aggregate_effective": tau_aggregate,
        "ratio_aggregate_over_compact": ratio,
        "diag": diag,
    }


def plot_compact_vs_aggregate_tau(
    comparison: dict[str, Any],
    ax: Any | None = None,
) -> Any:
    """
    Plot compact and aggregate-effective vertical haze optical depths.

    Returns the matplotlib axis.
    """
    import matplotlib.pyplot as plt

    if ax is None:
        _, ax = plt.subplots(figsize=(7.6, 4.8))

    wave = np.asarray(comparison["wavelength_um"], dtype=float)
    tau_compact = np.asarray(comparison["tau_compact"], dtype=float)
    tau_aggregate = np.asarray(comparison["tau_aggregate_effective"], dtype=float)

    ax.loglog(
        wave,
        np.maximum(tau_compact, 1e-300),
        label="compact Mie",
    )

    ax.loglog(
        wave,
        np.maximum(tau_aggregate, 1e-300),
        label="aggregate effective Mie",
    )

    ax.set_xlabel("Wavelength [µm]")
    ax.set_ylabel("Vertical haze optical depth")
    ax.set_title("Compact vs aggregate-effective haze opacity")
    ax.legend()
    ax.grid(alpha=0.3, which="both")

    return ax


def plot_compact_vs_aggregate_ratio(
    comparison: dict[str, Any],
    ax: Any | None = None,
) -> Any:
    """
    Plot aggregate-effective optical depth divided by compact optical depth.

    Returns the matplotlib axis.
    """
    import matplotlib.pyplot as plt

    if ax is None:
        _, ax = plt.subplots(figsize=(7.6, 4.2))

    wave = np.asarray(comparison["wavelength_um"], dtype=float)
    ratio = np.asarray(comparison["ratio_aggregate_over_compact"], dtype=float)

    ax.semilogx(
        wave,
        ratio,
    )

    ax.axhline(
        1.0,
        linewidth=1,
    )

    ax.set_xlabel("Wavelength [µm]")
    ax.set_ylabel("Aggregate effective / compact")
    ax.set_title("Aggregate-effective opacity ratio")
    ax.grid(alpha=0.3, which="both")

    return ax

