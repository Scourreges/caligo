def run_clear_and_hazy_transmission(
    case: Any,
    opacity_ck: Any,
    cloud_df: pd.DataFrame,
    calculation: str = "transmission",
    full_output: bool = True,
) -> dict[str, Any]:
    """Run clear and hazy PICASO transmission spectra for one cloud table."""
    clear_cloud_df = cloud_df.copy()
    clear_cloud_df["opd"] = 0.0

    case.clouds(df=clear_cloud_df)
    clear = case.spectrum(opacity_ck, calculation=calculation, full_output=full_output)

    case.clouds(df=cloud_df)
    hazy = case.spectrum(opacity_ck, calculation=calculation, full_output=full_output)

    required = {"wavenumber", "transit_depth"}

    for label, spectrum in [("clear", clear), ("hazy", hazy)]:
        missing = required - set(spectrum.keys())
        if missing:
            raise KeyError(
                f"The {label} PICASO spectrum is missing keys {sorted(missing)}. "
                f"Available keys: {list(spectrum.keys())}"
            )

    wno_clear = np.asarray(clear["wavenumber"], dtype=float)
    td_clear = np.asarray(clear["transit_depth"], dtype=float)
    wno_hazy = np.asarray(hazy["wavenumber"], dtype=float)
    td_hazy = np.asarray(hazy["transit_depth"], dtype=float)

    wave_clear_um = 1e4 / wno_clear
    wave_hazy_um = 1e4 / wno_hazy

    order_clear = np.argsort(wave_clear_um)
    order_hazy = np.argsort(wave_hazy_um)

    wave_clear_um = wave_clear_um[order_clear]
    td_clear = td_clear[order_clear]
    wave_hazy_um = wave_hazy_um[order_hazy]
    td_hazy = td_hazy[order_hazy]

    clear_interp = np.interp(wave_hazy_um, wave_clear_um, td_clear)
    delta = td_hazy - clear_interp

    return {
        "clear": clear,
        "hazy": hazy,
        "wavelength_clear_um": wave_clear_um,
        "transit_depth_clear": td_clear,
        "wavelength_hazy_um": wave_hazy_um,
        "transit_depth_hazy": td_hazy,
        "transit_depth_clear_interp": clear_interp,
        "haze_minus_clear": delta,
        "transit_depth_clear_ppm": td_clear * 1e6,
        "transit_depth_hazy_ppm": td_hazy * 1e6,
        "haze_minus_clear_ppm": delta * 1e6,
    }



