@dataclass
class RefractiveIndexTable:
    """Container for wavelength-dependent complex refractive indices."""

    wavelength_um: np.ndarray
    n: np.ndarray
    k: np.ndarray
    source: str
    mapping: str
    wave_unit: str

    @property
    def m_complex(self) -> np.ndarray:
        return self.n + 1j * self.k


def _load_numeric_table(path: str | Path) -> np.ndarray:
    """
    Load rows containing at least three numeric values.

    This deliberately ignores header/comment text and keeps only numeric rows,
    which makes it tolerant of several refractive-index table styles.
    """
    path = Path(path)

    if not path.exists():
        raise FileNotFoundError(f"Could not find refractive-index file: {path}")

    rows: list[list[float]] = []

    with path.open("r") as handle:
        for line in handle:
            stripped = line.strip()

            if not stripped:
                continue

            if stripped.startswith(("#", "!", "%")):
                continue

            # Make comma-separated files work too.
            tokens = stripped.replace(",", " ").split()
            values: list[float] = []

            for token in tokens:
                try:
                    values.append(float(token))
                except ValueError:
                    pass

            if len(values) >= 3:
                rows.append(values)

    if not rows:
        raise ValueError(f"No numeric rows were found in {path}.")

    ncols = max(len(row) for row in rows)
    table = np.full((len(rows), ncols), np.nan, dtype=float)

    for i, row in enumerate(rows):
        table[i, : len(row)] = row

    return table


def _convert_wave_to_um(wave: np.ndarray, unit: str) -> np.ndarray:
    """Convert a wavelength array to microns."""
    unit_clean = unit.lower().strip()

    if unit_clean in {"um", "micron", "microns", "micrometer", "micrometers"}:
        return wave.copy()

    if unit_clean in {"nm", "nanometer", "nanometers"}:
        return wave * 1e-3

    if unit_clean in {"angstrom", "angstroms", "a"}:
        return wave * 1e-4

    raise ValueError(
        "wave_unit must be 'micron', 'nanometer', 'angstrom', or 'auto'."
    )


def _score_refrind_candidate(wave_um: np.ndarray, n: np.ndarray, k: np.ndarray) -> float:
    """Score a possible refractive-index column/unit interpretation."""
    good = np.isfinite(wave_um) & np.isfinite(n) & np.isfinite(k)
    good &= wave_um > 0
    good &= n > 0
    good &= k >= 0

    if np.sum(good) < 3:
        return -1e9

    wave = wave_um[good]
    n_good = n[good]
    k_good = k[good]

    order = np.argsort(wave)
    wave = wave[order]
    n_good = n_good[order]
    k_good = k_good[order]

    unique_wave = np.unique(wave)

    if unique_wave.size < 3:
        return -1e9

    score = 0.0

    # Reasonable refractive-index values.
    if 0.5 <= np.nanmedian(n_good) <= 5.0:
        score += 20.0
    else:
        score -= 40.0

    if np.nanmin(k_good) >= 0.0 and np.nanmedian(k_good) <= 10.0:
        score += 15.0
    else:
        score -= 40.0

    # A useful wavelength range for planetary spectra.
    wave_min = float(np.nanmin(wave))
    wave_max = float(np.nanmax(wave))

    if 1e-3 <= wave_min < wave_max <= 1e4:
        score += 10.0

    if wave_min < 30.0 and wave_max > 0.1:
        score += 20.0

    if wave_min <= 0.5 and wave_max >= 5.0:
        score += 10.0

    # Prefer mostly sorted wavelength columns.
    diffs = np.diff(wave)
    if np.all(diffs > 0):
        score += 10.0

    score += min(float(unique_wave.size), 100.0) / 10.0

    return score


def read_refractive_index_file(
    path: str | Path,
    mapping: str = "auto",
    wave_unit: str = "auto",
    verbose: bool = True,
) -> RefractiveIndexTable:
    """
    Read a refractive-index file and infer wavelength, n, and k columns.

    Parameters
    ----------
    path
        Path to a refractive-index file. Common VIRGA-style .refrind tables
        are supported.
    mapping
        Either 'auto', 'col1_wave_col2_n_col3_k', or
        'col0_wave_col1_n_col2_k'. The first option is useful for files where
        column 0 is an index and columns 1, 2, 3 are wavelength, n, k.
    wave_unit
        'auto', 'micron', 'nanometer', or 'angstrom'.
    verbose
        Print the selected mapping.
    """
    path = Path(path)
    table = _load_numeric_table(path)

    candidates: list[tuple[float, str, str, np.ndarray, np.ndarray, np.ndarray]] = []

    mapping_specs: list[tuple[str, int, int, int]] = []

    if mapping == "auto":
        if table.shape[1] >= 4:
            mapping_specs.append(("col1_wave_col2_n_col3_k", 1, 2, 3))
        if table.shape[1] >= 3:
            mapping_specs.append(("col0_wave_col1_n_col2_k", 0, 1, 2))
    elif mapping == "col1_wave_col2_n_col3_k":
        mapping_specs.append((mapping, 1, 2, 3))
    elif mapping == "col0_wave_col1_n_col2_k":
        mapping_specs.append((mapping, 0, 1, 2))
    else:
        raise ValueError(
            "mapping must be 'auto', 'col1_wave_col2_n_col3_k', "
            "or 'col0_wave_col1_n_col2_k'."
        )

    unit_specs: list[str]

    if wave_unit == "auto":
        unit_specs = ["micron", "nanometer", "angstrom"]
    else:
        unit_specs = [wave_unit]

    for map_name, i_wave, i_n, i_k in mapping_specs:
        if max(i_wave, i_n, i_k) >= table.shape[1]:
            continue

        raw_wave = np.asarray(table[:, i_wave], dtype=float)
        n = np.asarray(table[:, i_n], dtype=float)
        k = np.asarray(table[:, i_k], dtype=float)

        for unit in unit_specs:
            wave_um = _convert_wave_to_um(raw_wave, unit)
            score = _score_refrind_candidate(wave_um, n, k)
            candidates.append((score, map_name, unit, wave_um, n, k))

    if not candidates:
        raise ValueError(f"Could not interpret refractive-index table: {path}")

    candidates.sort(key=lambda item: item[0], reverse=True)
    best_score, best_mapping, best_unit, wave_um, n, k = candidates[0]

    if best_score < 0:
        raise ValueError(
            "Could not find a plausible wavelength/n/k mapping for "
            f"{path}. Best candidate score was {best_score:.2f}."
        )

    good = np.isfinite(wave_um) & np.isfinite(n) & np.isfinite(k)
    good &= wave_um > 0
    good &= n > 0
    good &= k >= 0

    wave_um = wave_um[good]
    n = n[good]
    k = k[good]

    order = np.argsort(wave_um)
    wave_um = wave_um[order]
    n = n[order]
    k = k[order]

    unique_wave, unique_indices = np.unique(wave_um, return_index=True)
    wave_um = unique_wave
    n = n[unique_indices]
    k = k[unique_indices]

    if wave_um.size < 3:
        raise ValueError(
            "The selected refractive-index table has fewer than three unique "
            "wavelength points."
        )

    if verbose:
        print("Selected refractive-index mapping:", best_mapping)
        print("Selected wavelength unit:", best_unit)
        print("Wavelength range [um]:", float(wave_um.min()), "to", float(wave_um.max()))
        print("n range:", float(n.min()), "to", float(n.max()))
        print("k range:", float(k.min()), "to", float(k.max()))

    return RefractiveIndexTable(
        wavelength_um=wave_um,
        n=n,
        k=k,
        source=str(path),
        mapping=best_mapping,
        wave_unit=best_unit,
    )


def interpolate_refractive_index(
    refrind: RefractiveIndexTable,
    wavelength_um: np.ndarray,
    extrapolate: str = "clamp",
) -> tuple[np.ndarray, dict[str, Any]]:
    """
    Interpolate a refractive-index table onto a wavelength grid.

    Interpolation is done in log-wavelength space. By default, values outside
    the refractive-index table are clamped to the nearest table edge and a
    diagnostic is returned.
    """
    wavelength_um = _as_array_1d(wavelength_um, "wavelength_um")

    if not np.all(wavelength_um > 0):
        raise ValueError("wavelength_um must be positive.")

    wave_tab = refrind.wavelength_um
    log_wave_tab = safe_log10(wave_tab)
    log_wave = safe_log10(wavelength_um)

    outside_low = wavelength_um < wave_tab.min()
    outside_high = wavelength_um > wave_tab.max()
    outside = outside_low | outside_high

    if np.any(outside) and extrapolate == "raise":
        raise ValueError(
            "Requested wavelengths extend outside the refractive-index table: "
            f"requested [{wavelength_um.min():.3e}, {wavelength_um.max():.3e}] um, "
            f"table [{wave_tab.min():.3e}, {wave_tab.max():.3e}] um."
        )

    if extrapolate not in {"clamp", "raise"}:
        raise ValueError("extrapolate must be 'clamp' or 'raise'.")

    n_interp = np.interp(log_wave, log_wave_tab, refrind.n)
    k_interp = np.interp(log_wave, log_wave_tab, refrind.k)

    diag = {
        "refractive_index_source": refrind.source,
        "refractive_index_mapping": refrind.mapping,
        "refractive_index_wave_unit": refrind.wave_unit,
        "refractive_index_wave_min_um": float(wave_tab.min()),
        "refractive_index_wave_max_um": float(wave_tab.max()),
        "requested_wave_min_um": float(wavelength_um.min()),
        "requested_wave_max_um": float(wavelength_um.max()),
        "n_wavelength_outside_refrind": int(np.sum(outside)),
        "fraction_wavelength_outside_refrind": float(np.mean(outside)),
    }

    return n_interp + 1j * k_interp, diag


