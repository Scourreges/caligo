def _import_miepython():
    try:
        import miepython  # type: ignore
    except Exception as exc:  # pragma: no cover
        raise ImportError(
            "Direct Mie calculations require miepython. Install it with "
            "'conda install -c conda-forge miepython' or 'pip install miepython'."
        ) from exc

    return miepython


def _mie_efficiencies_one(m_complex: complex, radius_um: float, wavelength_um: float) -> tuple[float, float, float]:
    """
    Return Qext, Qsca, and asymmetry parameter g for one sphere.

    This wrapper supports several miepython API versions.
    """
    miepython = _import_miepython()

    radius_um = float(radius_um)
    wavelength_um = float(wavelength_um)

    if radius_um <= 0 or wavelength_um <= 0:
        raise ValueError("radius_um and wavelength_um must be positive.")

    x = 2.0 * np.pi * radius_um / wavelength_um

    if hasattr(miepython, "efficiencies_mx"):
        out = miepython.efficiencies_mx(m_complex, x)
        qext, qsca, _qback, g = out[:4]
        return float(qext), float(qsca), float(g)

    if hasattr(miepython, "efficiencies"):
        # Newer miepython versions use diameter and wavelength in the same units.
        diameter_um = 2.0 * radius_um
        out = miepython.efficiencies(m_complex, diameter_um, wavelength_um)
        qext, qsca, _qback, g = out[:4]
        return float(qext), float(qsca), float(g)

    if hasattr(miepython, "mie"):
        out = miepython.mie(m_complex, x)
        qext, qsca, _qback, g = out[:4]
        return float(qext), float(qsca), float(g)

    raise AttributeError(
        "Could not find a supported Mie-efficiency function in miepython."
    )


def _maxwell_garnett_effective_index(m_solid: np.ndarray, filling_factor: np.ndarray) -> np.ndarray:
    """
    Maxwell-Garnett effective refractive index for solid inclusions in vacuum.

    This is an approximate porous-sphere treatment. It is useful for a first
    aggregate sensitivity test, but it is not a replacement for true fractal
    aggregate optics.
    """
    m_solid = np.asarray(m_solid, dtype=complex)
    filling_factor = np.asarray(filling_factor, dtype=float)
    filling_factor = np.clip(filling_factor, 0.0, 1.0)

    eps_i = m_solid**2
    eps_m = 1.0 + 0.0j

    numerator = eps_i + 2.0 * eps_m + 2.0 * filling_factor * (eps_i - eps_m)
    denominator = eps_i + 2.0 * eps_m - filling_factor * (eps_i - eps_m)
    eps_eff = eps_m * numerator / denominator

    return np.sqrt(eps_eff)


def _select_radius_matrix_cm(ctx: dict[str, Any], optics_mode: str) -> np.ndarray:
    """Return a radius matrix with shape (nz, n_bin) in cm."""
    nz = int(ctx["nz"])
    n_bin = int(ctx["n_bin"])

    r_compact_cm = np.asarray(ctx["r_compact_cm"], dtype=float)

    if r_compact_cm.shape != (n_bin,):
        raise ValueError("ctx['r_compact_cm'] must have shape (n_bin,).")

    if optics_mode == "compact_mie":
        return np.repeat(r_compact_cm[None, :], nz, axis=0)

    if optics_mode == "aggregate_effective_mie":
        if "r_fractal_layer_cm" not in ctx:
            raise KeyError(
                "aggregate_effective_mie requires ctx['r_fractal_layer_cm']."
            )

        r_fractal = np.asarray(ctx["r_fractal_layer_cm"], dtype=float)

        if r_fractal.shape != (nz, n_bin):
            raise ValueError(
                "ctx['r_fractal_layer_cm'] must have shape (nz, n_bin)."
            )

        can_be_aggregate = np.asarray(
            ctx.get("can_be_aggregate", np.zeros(n_bin, dtype=bool)),
            dtype=bool,
        )

        w_agg_layer = np.asarray(ctx.get("w_agg_layer", np.zeros(nz)), dtype=float)
        w_agg_matrix = np.clip(w_agg_layer[:, None], 0.0, 1.0) * can_be_aggregate[None, :]

        return (1.0 - w_agg_matrix) * r_compact_cm[None, :] + w_agg_matrix * r_fractal

    if optics_mode == "aggregate_table":
        raise NotImplementedError(
            "aggregate_table is a planned mode. For now use compact_mie or "
            "aggregate_effective_mie."
        )

    raise ValueError(
        "optics_mode must be 'compact_mie', 'aggregate_effective_mie', "
        "or 'aggregate_table'."
    )


def compute_layer_optical_properties(
    result: dict[str, Any],
    refractive_index_file: str | Path,
    wavelength_um: np.ndarray | None = None,
    n_wavelength: int | None = None,
    wavelength_range_um: tuple[float, float] | None = None,
    optics_mode: str = "compact_mie",
    refrind_mapping: str = "auto",
    refrind_wave_unit: str = "auto",
    refrind_extrapolate: str = "clamp",
    verbose: bool = True,
) -> dict[str, Any]:
    """
    Compute layer optical properties from a finished Caligo result.

    Returns a dictionary containing optical-depth arrays on the Caligo
    microphysics grid.
    """
    if "ctx" not in result:
        raise KeyError("result must contain result['ctx'].")

    if "n" not in result:
        raise KeyError("result must contain result['n'].")

    ctx = result["ctx"]
    n_state = np.asarray(result["n"], dtype=float)

    Pbar = np.asarray(ctx["Pbar"], dtype=float)
    dz = np.asarray(ctx["dz"], dtype=float)
    n_bin = int(ctx["n_bin"])
    nz = int(ctx["nz"])

    if n_state.shape != (nz, n_bin):
        raise ValueError(
            "result['n'] has the wrong shape. "
            f"Got {n_state.shape}, expected {(nz, n_bin)}."
        )

    if dz.shape != (nz,):
        raise ValueError("ctx['dz'] must have shape (nz,).")

    refrind = read_refractive_index_file(
        refractive_index_file,
        mapping=refrind_mapping,
        wave_unit=refrind_wave_unit,
        verbose=verbose,
    )

    if wavelength_um is None:
        if wavelength_range_um is not None:
            lo, hi = wavelength_range_um
            if lo <= 0 or hi <= lo:
                raise ValueError("wavelength_range_um must be (positive_min, larger_max).")

            nwave = int(n_wavelength or 180)
            wavelength_um = np.logspace(np.log10(lo), np.log10(hi), nwave)
        elif n_wavelength is not None:
            wavelength_um = np.logspace(
                np.log10(float(refrind.wavelength_um.min())),
                np.log10(float(refrind.wavelength_um.max())),
                int(n_wavelength),
            )
        else:
            wavelength_um = refrind.wavelength_um.copy()
    else:
        wavelength_um = _as_array_1d(wavelength_um, "wavelength_um")

    wavelength_um = _as_array_1d(wavelength_um, "wavelength_um")

    if not np.all(wavelength_um > 0):
        raise ValueError("wavelength_um must be positive.")

    order_wave = np.argsort(wavelength_um)
    wavelength_um = wavelength_um[order_wave]

    m_solid_wave, refrind_diag = interpolate_refractive_index(
        refrind,
        wavelength_um,
        extrapolate=refrind_extrapolate,
    )

    radius_matrix_cm = _select_radius_matrix_cm(ctx, optics_mode)

    if radius_matrix_cm.shape != (nz, n_bin):
        raise ValueError("Internal radius matrix has the wrong shape.")

    rho_haze = float(ctx.get("rho_haze", 1.0))

    if optics_mode == "aggregate_effective_mie":
        rho_eff_layer = np.asarray(ctx["rho_eff_layer"], dtype=float)

        if rho_eff_layer.shape != (nz, n_bin):
            raise ValueError("ctx['rho_eff_layer'] must have shape (nz, n_bin).")

        filling_matrix = np.clip(rho_eff_layer / max(rho_haze, 1e-300), 0.0, 1.0)

        can_be_aggregate = np.asarray(
            ctx.get("can_be_aggregate", np.zeros(n_bin, dtype=bool)),
            dtype=bool,
        )

        w_agg_layer = np.asarray(ctx.get("w_agg_layer", np.zeros(nz)), dtype=float)
        w_agg_matrix = np.clip(w_agg_layer[:, None], 0.0, 1.0) * can_be_aggregate[None, :]
    else:
        filling_matrix = np.ones((nz, n_bin), dtype=float)
        w_agg_matrix = np.zeros((nz, n_bin), dtype=float)

    nwave = wavelength_um.size

    opd = np.zeros((nz, nwave), dtype=float)
    tau_sca = np.zeros((nz, nwave), dtype=float)
    g_num = np.zeros((nz, nwave), dtype=float)

    total_number_column_by_bin = np.sum(n_state * dz[:, None], axis=0)
    total_mass_column_by_bin = np.sum(
        n_state * np.asarray(ctx["m_bin"], dtype=float)[None, :] * dz[:, None],
        axis=0,
    )

    if verbose:
        r_min_nm = float(np.nanmin(radius_matrix_cm) * 1e7)
        r_max_nm = float(np.nanmax(radius_matrix_cm) * 1e7)
        print("Direct Caligo-grid Mie calculation")
        print("  optics_mode:", optics_mode)
        print("  layers:", nz)
        print("  bins:", n_bin)
        print("  wavelength points:", nwave)
        print("  wavelength range [um]:", float(wavelength_um.min()), "to", float(wavelength_um.max()))
        print("  optical radius range [nm]:", r_min_nm, "to", r_max_nm)

    for k in range(nz):
        number_column = n_state[k, :] * dz[k]

        if not np.any(number_column > 0):
            continue

        for i in range(n_bin):
            Ncol = float(number_column[i])

            if Ncol <= 0 or not np.isfinite(Ncol):
                continue

            radius_cm = float(radius_matrix_cm[k, i])

            if radius_cm <= 0 or not np.isfinite(radius_cm):
                continue

            radius_um = radius_cm * 1e4
            area_cm2 = np.pi * radius_cm**2

            if optics_mode == "aggregate_effective_mie":
                f_fill = float(filling_matrix[k, i])
                m_wave = _maxwell_garnett_effective_index(m_solid_wave, f_fill)
            else:
                m_wave = m_solid_wave

            for j, wave_um in enumerate(wavelength_um):
                qext, qsca, g = _mie_efficiencies_one(m_wave[j], radius_um, float(wave_um))

                if qext < 0 or qsca < 0:
                    continue

                qsca = min(qsca, qext) if qext > 0 else 0.0

                tau_ext_ij = Ncol * area_cm2 * qext
                tau_sca_ij = Ncol * area_cm2 * qsca

                opd[k, j] += tau_ext_ij
                tau_sca[k, j] += tau_sca_ij
                g_num[k, j] += tau_sca_ij * g

    w0 = np.zeros_like(opd)
    g0 = np.zeros_like(opd)

    positive_tau = opd > 0
    w0[positive_tau] = tau_sca[positive_tau] / opd[positive_tau]
    w0 = np.clip(w0, 0.0, 1.0)

    positive_sca = tau_sca > 0
    g0[positive_sca] = g_num[positive_sca] / tau_sca[positive_sca]
    g0 = np.clip(g0, -1.0, 1.0)

    wavenumber = 1e4 / wavelength_um

    vertical_tau = np.sum(opd, axis=0)

    diag = {
        "optics_mode": optics_mode,
        "n_layer_micro": int(nz),
        "n_bin": int(n_bin),
        "n_wavelength": int(nwave),
        "wavelength_min_um": float(wavelength_um.min()),
        "wavelength_max_um": float(wavelength_um.max()),
        "radius_min_nm": float(np.nanmin(radius_matrix_cm) * 1e7),
        "radius_max_nm": float(np.nanmax(radius_matrix_cm) * 1e7),
        "max_vertical_tau": float(np.nanmax(vertical_tau)),
        "min_vertical_tau": float(np.nanmin(vertical_tau)),
        "total_number_column_cm2": float(np.sum(total_number_column_by_bin)),
        "total_mass_column_g_cm2": float(np.sum(total_mass_column_by_bin)),
    }

    diag.update(refrind_diag)

    if optics_mode == "aggregate_effective_mie":
        diag.update(
            {
                "filling_factor_min": float(np.nanmin(filling_matrix)),
                "filling_factor_max": float(np.nanmax(filling_matrix)),
                "aggregate_weight_min": float(np.nanmin(w_agg_matrix)),
                "aggregate_weight_max": float(np.nanmax(w_agg_matrix)),
                "aggregate_effective_mie_note": (
                    "Porous-sphere Maxwell-Garnett approximation. "
                    "Not a full fractal-aggregate scattering calculation."
                ),
            }
        )

    if verbose:
        print("  vertical tau range:", float(np.nanmin(vertical_tau)), "to", float(np.nanmax(vertical_tau)))
        if diag["fraction_wavelength_outside_refrind"] > 0:
            print(
                "  warning: fraction of requested wavelengths outside refrind table:",
                diag["fraction_wavelength_outside_refrind"],
            )

    return {
        "ctx": ctx,
        "Pbar_micro": Pbar,
        "dz_micro": dz,
        "n_state": n_state,
        "wavelength_um": wavelength_um,
        "wavenumber": wavenumber,
        "opd_micro": opd,
        "w0_micro": w0,
        "g0_micro": g0,
        "vertical_tau": vertical_tau,
        "radius_matrix_cm": radius_matrix_cm,
        "diag": diag,
    }


