"""
Caligo optical post-processing utilities.

This module connects a finished Caligo haze-microphysics run to PICASO
without forcing the particles onto a narrow VIRGA .mieff radius grid.

Main idea
---------
Caligo already knows the particle sizes and number densities. The optical
post-processing should therefore calculate optical properties on the Caligo
particle grid itself, then format the resulting layer optical depths for
PICASO.

Current modes
-------------
compact_mie
    Spherical Mie calculation using the compact-equivalent Caligo radius.
    This is the recommended baseline.

aggregate_effective_mie
    Experimental porous-sphere approximation. The aggregate radius and
    effective density from the Caligo context are used to estimate a filling
    factor, then a Maxwell-Garnett effective refractive index is used in a
    Mie calculation. This is not a full fractal-aggregate scattering model.

aggregate_table
    Placeholder for a future table generated by a true aggregate scattering
    method such as multi-sphere T-matrix or DDA.

Dependencies
------------
Requires numpy and pandas. Direct Mie calculations require miepython.
Install with, for example:

    conda install -c conda-forge miepython

or

    pip install miepython
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Any
import warnings

import numpy as np
import pandas as pd


