def safe_log10(x: Any) -> np.ndarray:
    """Return log10(x) with a tiny positive floor."""
    return np.log10(np.maximum(np.asarray(x, dtype=float), 1e-300))


def _as_array_1d(values: Any, name: str) -> np.ndarray:
    """Convert an input to a finite 1D float array."""
    arr = np.asarray(values, dtype=float)

    if arr.ndim != 1:
        raise ValueError(f"{name} must be one-dimensional. Got shape {arr.shape}.")

    if arr.size == 0:
        raise ValueError(f"{name} is empty.")

    if not np.all(np.isfinite(arr)):
        raise ValueError(f"{name} contains non-finite values.")

    return arr


def _faces_from_centers_ascending(x_center: np.ndarray) -> np.ndarray:
    """Build interval faces from ascending cell centers."""
    x_center = _as_array_1d(x_center, "x_center")

    if x_center.size < 2:
        dx = max(abs(float(x_center[0])) * 0.05, 0.05)
        return np.array([x_center[0] - dx, x_center[0] + dx], dtype=float)

    if not np.all(np.diff(x_center) > 0):
        raise ValueError("x_center must be strictly increasing.")

    faces = np.empty(x_center.size + 1, dtype=float)
    faces[1:-1] = 0.5 * (x_center[:-1] + x_center[1:])
    faces[0] = x_center[0] - 0.5 * (x_center[1] - x_center[0])
    faces[-1] = x_center[-1] + 0.5 * (x_center[-1] - x_center[-2])

    return faces


