def remap_layer_optics_by_log_pressure(
    Pbar_src: np.ndarray,
    opd_src: np.ndarray,
    w0_src: np.ndarray,
    g0_src: np.ndarray,
    Pbar_dst: np.ndarray,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, dict[str, Any]]:
    """
    Conservatively remap layer optical properties between pressure grids.

    Optical depth is distributed by overlap in log-pressure space. Scattering
    optical depth and g-weighted scattering optical depth are remapped in the
    same way, then w0 and g0 are reconstructed.
    """
    Pbar_src = _as_array_1d(Pbar_src, "Pbar_src")
    Pbar_dst = _as_array_1d(Pbar_dst, "Pbar_dst")

    opd_src = np.asarray(opd_src, dtype=float)
    w0_src = np.asarray(w0_src, dtype=float)
    g0_src = np.asarray(g0_src, dtype=float)

    if opd_src.shape != w0_src.shape or opd_src.shape != g0_src.shape:
        raise ValueError("opd_src, w0_src, and g0_src must have the same shape.")

    if opd_src.shape[0] != Pbar_src.size:
        raise ValueError("opd_src first axis must match Pbar_src.")

    nwave = opd_src.shape[1]

    src_order = np.argsort(safe_log10(Pbar_src))
    dst_order = np.argsort(safe_log10(Pbar_dst))

    logP_src_sorted = safe_log10(Pbar_src[src_order])
    logP_dst_sorted = safe_log10(Pbar_dst[dst_order])

    src_faces = _faces_from_centers_ascending(logP_src_sorted)
    dst_faces = _faces_from_centers_ascending(logP_dst_sorted)

    opd_dst_sorted = np.zeros((Pbar_dst.size, nwave), dtype=float)
    sca_dst_sorted = np.zeros_like(opd_dst_sorted)
    gnum_dst_sorted = np.zeros_like(opd_dst_sorted)

    opd_src_sorted = opd_src[src_order, :]
    sca_src_sorted = opd_src_sorted * np.clip(w0_src[src_order, :], 0.0, 1.0)
    gnum_src_sorted = sca_src_sorted * np.clip(g0_src[src_order, :], -1.0, 1.0)

    source_tau_total = float(np.sum(opd_src_sorted))
    assigned_tau_total = 0.0

    j_start = 0

    for i in range(Pbar_src.size):
        left = src_faces[i]
        right = src_faces[i + 1]
        width = max(right - left, 1e-300)

        while j_start < Pbar_dst.size and dst_faces[j_start + 1] <= left:
            j_start += 1

        j = j_start

        while j < Pbar_dst.size and dst_faces[j] < right:
            overlap = max(0.0, min(right, dst_faces[j + 1]) - max(left, dst_faces[j]))

            if overlap > 0:
                frac = overlap / width
                opd_dst_sorted[j, :] += opd_src_sorted[i, :] * frac
                sca_dst_sorted[j, :] += sca_src_sorted[i, :] * frac
                gnum_dst_sorted[j, :] += gnum_src_sorted[i, :] * frac
                assigned_tau_total += float(np.sum(opd_src_sorted[i, :] * frac))

            j += 1

    w0_dst_sorted = np.zeros_like(opd_dst_sorted)
    g0_dst_sorted = np.zeros_like(opd_dst_sorted)

    positive_tau = opd_dst_sorted > 0
    w0_dst_sorted[positive_tau] = sca_dst_sorted[positive_tau] / opd_dst_sorted[positive_tau]
    w0_dst_sorted = np.clip(w0_dst_sorted, 0.0, 1.0)

    positive_sca = sca_dst_sorted > 0
    g0_dst_sorted[positive_sca] = gnum_dst_sorted[positive_sca] / sca_dst_sorted[positive_sca]
    g0_dst_sorted = np.clip(g0_dst_sorted, -1.0, 1.0)

    inv_dst_order = np.empty_like(dst_order)
    inv_dst_order[dst_order] = np.arange(dst_order.size)

    opd_dst = opd_dst_sorted[inv_dst_order, :]
    w0_dst = w0_dst_sorted[inv_dst_order, :]
    g0_dst = g0_dst_sorted[inv_dst_order, :]

    diag = {
        "source_tau_total_all_wavelengths": source_tau_total,
        "assigned_tau_total_all_wavelengths": float(assigned_tau_total),
        "lost_tau_fraction_all_wavelengths": float(
            max(source_tau_total - assigned_tau_total, 0.0)
            / max(source_tau_total, 1e-300)
        ),
    }

    return opd_dst, w0_dst, g0_dst, diag


def format_picaso_cloud_dataframe(
    opd: np.ndarray,
    w0: np.ndarray,
    g0: np.ndarray,
    pressure: np.ndarray,
    wavenumber: np.ndarray,
    use_virga_format: bool = False,
) -> pd.DataFrame:
    """Build a PICASO cloud dataframe from layer optical properties."""
    pressure = _as_array_1d(pressure, "pressure")
    wavenumber = _as_array_1d(wavenumber, "wavenumber")

    opd = np.asarray(opd, dtype=float)
    w0 = np.asarray(w0, dtype=float)
    g0 = np.asarray(g0, dtype=float)

    expected_shape = (pressure.size, wavenumber.size)

    if opd.shape != expected_shape:
        raise ValueError(f"opd shape {opd.shape} does not match {expected_shape}.")

    if w0.shape != expected_shape or g0.shape != expected_shape:
        raise ValueError("w0 and g0 must match opd shape.")

    if use_virga_format:
        try:
            from virga import justdoit as vdi  # type: ignore
        except Exception as exc:
            raise ImportError(
                "use_virga_format=True requires virga. Set use_virga_format=False "
                "to use Caligo's local dataframe formatter."
            ) from exc

        return vdi.picaso_format(
            opd,
            w0,
            g0,
            pressure=pressure,
            wavenumber=wavenumber,
        )

    rows = []

    for k, p in enumerate(pressure):
        for j, wno in enumerate(wavenumber):
            rows.append(
                {
                    "pressure": float(p),
                    "wavenumber": float(wno),
                    "opd": float(max(opd[k, j], 0.0)),
                    "w0": float(np.clip(w0[k, j], 0.0, 1.0)),
                    "g0": float(np.clip(g0[k, j], -1.0, 1.0)),
                }
            )

    return pd.DataFrame(rows, columns=["pressure", "wavenumber", "opd", "w0", "g0"])


def build_picaso_cloud_from_caligo(
    result: dict[str, Any],
    case: Any,
    refractive_index_file: str | Path,
    wavelength_um: np.ndarray | None = None,
    n_wavelength: int | None = None,
    wavelength_range_um: tuple[float, float] | None = None,
    optics_mode: str = "compact_mie",
    attach_photochem_atmosphere: bool = True,
    pc_object: Any | None = None,
    refrind_mapping: str = "auto",
    refrind_wave_unit: str = "auto",
    refrind_extrapolate: str = "clamp",
    use_virga_format: bool = False,
    verbose: bool = True,
) -> tuple[pd.DataFrame, dict[str, Any]]:
    """
    Build a PICASO cloud table from a finished Caligo microphysics result.

    This is the main user-facing function for Caligo to PICASO post-processing.
    """
    atmosphere_df = None

    if attach_photochem_atmosphere:
        atmosphere_df = attach_photochem_atmosphere_to_picaso(
            case,
            pc_object=pc_object,
            verbose=verbose,
        )

    Pbar_picaso = picaso_layer_pressure_profile(case)

    optics = compute_layer_optical_properties(
        result=result,
        refractive_index_file=refractive_index_file,
        wavelength_um=wavelength_um,
        n_wavelength=n_wavelength,
        wavelength_range_um=wavelength_range_um,
        optics_mode=optics_mode,
        refrind_mapping=refrind_mapping,
        refrind_wave_unit=refrind_wave_unit,
        refrind_extrapolate=refrind_extrapolate,
        verbose=verbose,
    )

    opd_picaso, w0_picaso, g0_picaso, remap_diag = remap_layer_optics_by_log_pressure(
        optics["Pbar_micro"],
        optics["opd_micro"],
        optics["w0_micro"],
        optics["g0_micro"],
        Pbar_picaso,
    )

    cloud_df = format_picaso_cloud_dataframe(
        opd_picaso,
        w0_picaso,
        g0_picaso,
        pressure=Pbar_picaso,
        wavenumber=optics["wavenumber"],
        use_virga_format=use_virga_format,
    )

    expected_rows = len(Pbar_picaso) * len(optics["wavenumber"])

    if len(cloud_df) != expected_rows:
        raise RuntimeError(
            "PICASO cloud table has the wrong number of rows. "
            f"Got {len(cloud_df)}, expected {expected_rows}."
        )

    diag = dict(optics["diag"])
    diag.update(remap_diag)
    diag.update(
        {
            "picaso_cloud_layers": int(len(Pbar_picaso)),
            "picaso_cloud_rows": int(len(cloud_df)),
            "picaso_cloud_expected_rows": int(expected_rows),
            "use_virga_format": bool(use_virga_format),
        }
    )

    if atmosphere_df is not None:
        diag["picaso_atmosphere_levels"] = int(len(atmosphere_df))
        diag["picaso_atmosphere_columns"] = list(atmosphere_df.columns)

    out = {
        "cloud_df": cloud_df,
        "atmosphere_df": atmosphere_df,
        "Pbar_picaso": Pbar_picaso,
        "opd_picaso": opd_picaso,
        "w0_picaso": w0_picaso,
        "g0_picaso": g0_picaso,
        "wavelength_um": optics["wavelength_um"],
        "wavenumber": optics["wavenumber"],
        "vertical_tau_micro": optics["vertical_tau"],
        "optics": optics,
        "diag": diag,
    }

    if verbose:
        print("PICASO cloud table built.")
        print("  layers:", diag["picaso_cloud_layers"])
        print("  wavelengths:", diag["n_wavelength"])
        print("  rows:", diag["picaso_cloud_rows"])
        print("  lost tau fraction in pressure remap:", diag["lost_tau_fraction_all_wavelengths"])

    return cloud_df, out


