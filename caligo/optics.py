def find_photochem_object(case_like: Any) -> Any | None:
    """Find the photochem object cached by a Caligo case, if available."""
    photochem_result = getattr(case_like, "_photochem_result", None)

    if isinstance(photochem_result, dict):
        pc = photochem_result.get("pc")
        if pc is not None:
            return pc

    return None


def create_picaso_atmosphere_from_photochem(pc_object: Any) -> pd.DataFrame:
    """
    Export the final photochem atmosphere into a PICASO-ready dataframe.

    Photochem pressure is expected in dyn cm^-2. PICASO expects bar.
    Aerosol bookkeeping columns are skipped.
    """
    sol = pc_object.return_atmosphere()

    if "pressure" not in sol:
        raise KeyError("pc.return_atmosphere() did not return 'pressure'.")

    if "temperature" not in sol:
        raise KeyError("pc.return_atmosphere() did not return 'temperature'.")

    pressure_bar = np.asarray(sol["pressure"], dtype=float) / 1e6
    temperature = np.asarray(sol["temperature"], dtype=float)

    nlevel = pressure_bar.size

    atmosphere_data: dict[str, np.ndarray] = {
        "pressure": pressure_bar[::-1].copy(),
        "temperature": temperature[::-1].copy(),
    }

    for key, value in sol.items():
        if key in {"pressure", "temperature", "Kzz", "kzz", "KZZ"}:
            continue

        if "aer" in key.lower():
            continue

        arr = np.asarray(value, dtype=float)

        if arr.ndim != 1:
            continue

        if arr.size != nlevel:
            continue

        atmosphere_data[key] = arr[::-1].copy()

    atmosphere_df = pd.DataFrame(atmosphere_data)

    gas_columns = [
        col for col in atmosphere_df.columns if col not in {"pressure", "temperature"}
    ]

    if len(gas_columns) == 0:
        raise RuntimeError("No gas-abundance columns were exported from photochem.")

    return atmosphere_df


def _picaso_profile_dataframe(case_like: Any) -> pd.DataFrame | None:
    """Return the current PICASO atmosphere profile dataframe, if present."""
    try:
        profile = case_like.picaso.inputs["atmosphere"]["profile"]
    except Exception:
        return None

    if profile is None:
        return None

    if isinstance(profile, pd.DataFrame):
        return profile

    if hasattr(profile, "columns"):
        return pd.DataFrame(profile)

    if isinstance(profile, dict):
        return pd.DataFrame(profile)

    return None


def _profile_has_composition(profile: pd.DataFrame | None) -> bool:
    if profile is None:
        return False

    cols = set(profile.columns)

    if not {"pressure", "temperature"}.issubset(cols):
        return False

    gas_cols = [col for col in profile.columns if col not in {"pressure", "temperature", "kz", "Kzz", "kzz"}]

    return len(gas_cols) > 0


def attach_photochem_atmosphere_to_picaso(
    case_like: Any,
    pc_object: Any | None = None,
    verbose: bool = True,
) -> pd.DataFrame | None:
    """
    Attach the final photochem atmosphere to the wrapped PICASO case.

    If no photochem object is available but the current PICASO profile already
    has composition columns, the current profile is left unchanged.
    """
    if pc_object is None:
        pc_object = find_photochem_object(case_like)

    if pc_object is None:
        profile = _picaso_profile_dataframe(case_like)

        if _profile_has_composition(profile):
            if verbose:
                print("PICASO already has an atmosphere with composition columns.")
            return profile

        raise RuntimeError(
            "No photochem object was found, and the current PICASO atmosphere "
            "does not appear to contain gas abundances. Run case.run_photochem() "
            "first or pass pc_object explicitly."
        )

    atmosphere_df = create_picaso_atmosphere_from_photochem(pc_object)

    if verbose:
        gas_columns = [
            col for col in atmosphere_df.columns if col not in {"pressure", "temperature"}
        ]
        print("Attaching the final photochem atmosphere to PICASO.")
        print("  pressure levels:", len(atmosphere_df))
        print("  gas columns:", len(gas_columns))
        print("  first gas columns:", gas_columns[:15])

    case_like.atmosphere(atmosphere_df, verbose=False)

    return atmosphere_df


def picaso_layer_pressure_profile(case_like: Any) -> np.ndarray:
    """
    Return the PICASO cloud-layer pressure grid.

    PICASO stores atmospheres on pressure levels, while cloud tables need one
    value per layer, so there should be nlevel - 1 cloud pressures.
    """
    profile = _picaso_profile_dataframe(case_like)

    if profile is None or "pressure" not in profile.columns:
        raise RuntimeError("Could not find a PICASO pressure profile.")

    pressure_levels = np.asarray(profile["pressure"], dtype=float)
    expected_layers = int(case_like.picaso.nlevel) - 1

    if pressure_levels.size == expected_layers + 1:
        pressure_layers = pressure_levels[1:].copy()
    elif pressure_levels.size == expected_layers:
        pressure_layers = pressure_levels.copy()
    else:
        raise ValueError(
            "Unexpected PICASO pressure-grid size. "
            f"Found {pressure_levels.size}, expected {expected_layers + 1} "
            f"levels or {expected_layers} layers."
        )

    return pressure_layers


